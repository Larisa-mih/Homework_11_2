def str_func():
    """Функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами"""
    text = "привет, мир!"
    print(text.upper())

def str_func2():
    """Функция, которая делает заглавными первые буквы каждого слова в строке,
     поступившей на вход функции"""
    text = "привет, мир!"
    print(text.title())